n = 5
for i in range(n):            # start with row 'i' = 0
    for j in range(n):        # start with col 'j' = 0
        print('*', end=" ")   
    print() 

#   By default there is a newline character appended to the item being printed (end='\n'), and end='' is used to make it print on the same line.

# And print() prints an empty newline, which is necessary to keep on printing on the next line.

